#Addition
def add_numbers(a,b):
    add = a + b
    return add
print(add_numbers(12,20))

#Subtraction
def minus_numbers(a,b):
    sub = a - b
    return sub
print(minus_numbers(12,20))

#Division
def divide_numbers(a,b):
    divide = a/b
    return divide
print(divide_numbers(12,20))

#Multiplication
def multiply_numbers(a,b):
    multiply = a * b
    return multiply
print(multiply_numbers(12,20))

